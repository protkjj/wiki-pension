def build_template(cols, guide, title: str) -> bytes:
    """작성요령 시트 + 빈 명부(헤더) 시트로 구성된 xlsx 양식 바이트 생성."""
    wb = Workbook()
    ws = wb.active
    ws.title = "작성요령"
    ws.column_dimensions["A"].width = 90
    head = Font(name="맑은 고딕", size=13, bold=True, color="1F4E79")
    body = Font(name="맑은 고딕", size=10)
    ws["A1"] = f"[{title}] 작성요령"
    ws["A1"].font = head
    r = 3
    for line in guide:
        ws.cell(row=r, column=1, value=line).font = body
        ws.cell(row=r, column=1).alignment = Alignment(wrap_text=True, vertical="top")
        r += 1

    ws2 = wb.create_sheet(title[:28])
    fill = PatternFill("solid", fgColor="DCE6F1")
    hf = Font(name="맑은 고딕", size=10, bold=True)
    for j, c in enumerate(cols, start=1):
        cell = ws2.cell(row=1, column=j, value=c)
        cell.font = hf
        cell.fill = fill
        cell.alignment = Alignment(horizontal="center")
        ws2.column_dimensions[chr(64 + j) if j <= 26 else "AA"].width = 16
    ws2.freeze_panes = "A2"

    buf = io.BytesIO()
    wb.save(buf)
    return buf.getvalue()
